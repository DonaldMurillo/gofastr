// validateOutputDir rejects paths that would be unsafe to clean (the cwd, the
// FS root, absolute paths, parent traversals, or empty input).
func validateOutputDir(dir string) error {
	if strings.TrimSpace(dir) == "" {
		return fmt.Errorf("--out must not be empty")
	}
	clean := filepath.Clean(dir)
	if filepath.IsAbs(clean) {
		return fmt.Errorf("--out must be a relative path inside the project (got %q)", dir)
	}
	if clean == "." || clean == ".." {
		return fmt.Errorf("--out=%q would target the working directory; choose a subdirectory like gen/entities", dir)
	}
	for _, part := range strings.Split(clean, string(filepath.Separator)) {
		if part == ".." {
			return fmt.Errorf("--out=%q must not contain parent directory traversal", dir)
		}
	}
	return nil
}

// safeCleanOutputDir removes only the files this generator owns. It refuses to
// remove the directory if it contains files the generator did not write — this
// prevents accidental data loss when --out points at a populated directory.
func safeCleanOutputDir(dir string) error {
	if err := codegen.EnsureNoSymlinkPath(dir); err != nil {
		return err
	}
	info, err := os.Stat(dir)
	if err != nil {
		if os.IsNotExist(err) {
			return nil
		}
		return err
	}
	if !info.IsDir() {
		return fmt.Errorf("%s exists and is not a directory", dir)
	}
	entries, err := os.ReadDir(dir)
	if err != nil {
		return err
	}
	// Files the blueprint code generator owns at the output root. The current
	// layout is flat package main (main.go + app.go/screens.go/stubs.go/
	// resource.go/resource_test.go/e2e_test.go) plus the entities/ subtree; the
	// legacy "blueprint" dir and the bare register.go/models.go/... names are
	// retained so cleaning an output dir written by an older generator still
	// succeeds.
	owned := map[string]bool{
		".gitkeep": true, codegen.ManifestName: true,
		"main.go": true, "app.go": true, "screens.go": true, "stubs.go": true,
		"resource.go": true, "resource_test.go": true, "e2e_test.go": true,
		".env": true, ".gitignore": true,
		"register.go": true, "models.go": true, "columns.go": true, "repo.go": true, "events.go": true,
		"client": true, "entities": true, "blueprint": true,
	}
	for _, entry := range entries {
		if !owned[entry.Name()] {
			return fmt.Errorf("refusing to clean %s — contains unknown entry %q (move it or use --no-clean)", dir, entry.Name())
		}
	}
	for name := range owned {
		path := filepath.Join(dir, name)
		if name == ".gitkeep" {
			continue
		}
		if err := codegen.EnsureNoSymlinkPath(filepath.Dir(path)); err != nil {
			return err
		}
		if err := os.RemoveAll(path); err != nil && !os.IsNotExist(err) {
			return err
		}
	}
	return nil
}

