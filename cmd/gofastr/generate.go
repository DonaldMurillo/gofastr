func toCamelCase(s string) string {
	parts := strings.FieldsFunc(s, func(r rune) bool {
		return r == '_' || r == '-' || r == ' '
	})
	var result string
	for _, p := range parts {
		if len(p) > 0 {
			result += strings.ToUpper(p[:1]) + p[1:]
		}
	}
	return result
}

// toSnakeCase converts an entity name to snake_case for generated file names.
// It handles already-snake input ("blog_posts"), CamelCase ("BlogPost"), and
// hyphenated or spaced input, collapsing repeated separators.
func toSnakeCase(s string) string {
	var b strings.Builder
	runes := []rune(s)
	for i, r := range runes {
		isUpper := r >= 'A' && r <= 'Z'
		if i > 0 && isUpper {
			prev := runes[i-1]
			if (prev >= 'a' && prev <= 'z') || (prev >= '0' && prev <= '9') {
				b.WriteByte('_')
			}
		}
		switch {
		case r == '-' || r == ' ':
			b.WriteByte('_')
		case r >= 'A' && r <= 'Z':
			b.WriteRune(r + 'a' - 'A')
		default:
			b.WriteRune(r)
		}
	}
	out := b.String()
	for strings.Contains(out, "__") {
		out = strings.ReplaceAll(out, "__", "_")
	}
	return strings.Trim(out, "_")
}

// entityFileName maps an entity name to its generated file name, guarding
// against collisions with the fixed package files (register.go, shared.go,
// doc.go): a colliding name is prefixed with entity_ so it never shadows the
// seam or shared helpers.
func entityFileName(name string) string {
	snake := toSnakeCase(name)
	switch snake {
	case "register", "shared", "doc":
		return "entity_" + snake + ".go"
	}
	return snake + ".go"
}

