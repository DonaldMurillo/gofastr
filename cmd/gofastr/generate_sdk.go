// `gofastr generate sdk` emits downloadable client SDKs for the app's HTTP
// API: a standalone stdlib-only Go module (the same typed client the app
// embeds, packaged with its own go.mod) and a handrolled ESM client.js +
// client.d.ts pair — deliberately no npm packaging; publishing is the app
// owner's call. A dist/ directory holds the servable artifacts (sdk-go.zip,
// client.js, client.d.ts, manifest.json) that framework/sdkdocs hosts.
//
// Unlike `generate cli` (one-shot owned code), SDK output is generator-owned
// and regenerates IN PLACE: when the docs site warns that the artifacts
// drifted from the live schema, the fix is re-running this command, so a
// conflict prompt would only add friction. Point --out somewhere private if
// you need to keep an old build.

type sdkOptions struct {
	outDir     string
	name       string
	apiPrefix  string
	module     string
	baseURL    string
	sdkVersion string
	targets    []string
	only       []string
	exclude    []string
	noArchive  bool
	dryRun     bool
	json       bool
}

// sdkSpec is the derived input the SDK renderers consume: the shared
// per-entity model (buildEntityModel) plus the raw declarations for the
// emitters that mirror renderClient's exact field walk.
type sdkSpec struct {
	App            string
	SDKVersion     string
	GofastrVersion string
	Module         string
	BaseURL        string
	APIPrefix      string // "" or "/api" — baked into docs/examples, not client paths
	Decls          []framework.EntityDeclaration
	Entities       []cliEntity
}

// Header returns the DO-NOT-EDIT stamp shared by every generated SDK file.
func (s sdkSpec) Header() string {
	return fmt.Sprintf("Code generated by gofastr %s (sdk %s) for %s. DO NOT EDIT.",
		s.GofastrVersion, s.SDKVersion, s.App)
}

var sdkValidTargets = map[string]bool{"go": true, "js": true}

// runGenerateSDK implements `gofastr generate sdk`.
func runGenerateSDK(args []string) {
	opts, err := parseSDKOptions(args)
	if err != nil {
		fail("%v", err)
		info("Usage: gofastr generate sdk [--target=go,js] [--out=gen/sdk] [--name=<app>] [--api-prefix=api] [--only=a,b] [--exclude=c] [--module=<path>] [--base-url=<url>] [--sdk-version=<semver>] [--no-archive] [--dry-run] [--json]")
		osExit(1)
		return
	}
	applySDKConfigDefaults(&opts)

	decls, err := packReadEntities(".")
	if err != nil {
		fail("read entities: %v", err)
		osExit(1)
		return
	}
	if len(decls) == 0 {
		fail("no entities package found in the current directory")
		info("Run from your app root (the directory holding entities/), or generate the app first: gofastr generate --from=<blueprint>.")
		osExit(1)
		return
	}

	spec, err := buildSDKSpec(decls, &opts)
	if err != nil {
		if opts.dryRun && opts.json {
			printGeneratedErrorsJSON(err)
			osExit(1)
			return
		}
		fail("%v", err)
		osExit(1)
		return
	}
	if err := validateOutputDir(opts.outDir); err != nil {
		fail("%v", err)
		osExit(1)
		return
	}

	files, err := renderSDKFiles(spec, opts.targets, !opts.noArchive)
	if err != nil {
		fail("SDK generation failed: %v", err)
		osExit(1)
		return
	}

	if opts.dryRun {
		if opts.json {
			printGeneratedFilesJSON(files)
			return
		}
		info("Would generate %d file(s) in %s:", len(files), opts.outDir)
		for _, f := range files {
			fmt.Printf("    %s\n", filepath.Join(opts.outDir, f.name))
		}
		return
	}

	fileSet, err := fileSetFromGeneratedFiles(files, "sdk")
	if err != nil {
		fail("SDK generation failed: %v", err)
		osExit(1)
		return
	}
	writeOpts := codegen.WriteOptions{
		OutputRoot:   opts.outDir,
		SkipManifest: true,
		Conflict:     codegen.ConflictOverwrite,
	}
	if err := codegen.WriteFiles(fileSet, writeOpts); err != nil {
		fail("Failed to write generated files: %v", err)
		osExit(1)
		return
	}
	if opts.json {
		printGeneratedFilesJSON(files)
		return
	}
	success("Generated %d file(s) in %s", len(files), opts.outDir)
	fmt.Println()
	fmt.Println("  Next steps:")
	fmt.Printf("    serve the downloads: sdkdocs.Mount(site, app.Router(), sdkdocs.Config{Registry: app.Registry, Artifacts: os.DirFS(%q)})\n", filepath.ToSlash(filepath.Join(opts.outDir, "dist")))
	fmt.Println("    re-run this command whenever entities change — output regenerates in place")
}

func parseSDKOptions(args []string) (sdkOptions, error) {
	opts := sdkOptions{}
	for i := 0; i < len(args); i++ {
		arg := args[i]
		nextValue := func() (string, bool) {
			if i+1 < len(args) && !strings.HasPrefix(args[i+1], "--") {
				i++
				return args[i], true
			}
			return "", false
		}
		value := func(prefix string) (string, bool) {
			if strings.HasPrefix(arg, prefix+"=") {
				return strings.TrimPrefix(arg, prefix+"="), true
			}
			if arg == prefix {
				return nextValue()
			}
			return "", false
		}
		switch {
		case arg == "--no-archive":
			opts.noArchive = true
		case arg == "--dry-run":
			opts.dryRun = true
		case arg == "--json":
			opts.json = true
		default:
			if v, ok := value("--out"); ok {
				opts.outDir = v
			} else if v, ok := value("--name"); ok {
				opts.name = v
			} else if v, ok := value("--api-prefix"); ok {
				opts.apiPrefix = v
			} else if v, ok := value("--module"); ok {
				opts.module = v
			} else if v, ok := value("--base-url"); ok {
				opts.baseURL = v
			} else if v, ok := value("--sdk-version"); ok {
				opts.sdkVersion = v
			} else if v, ok := value("--target"); ok {
				opts.targets = splitCommaList(v)
			} else if v, ok := value("--only"); ok {
				opts.only = splitCommaList(v)
			} else if v, ok := value("--exclude"); ok {
				opts.exclude = splitCommaList(v)
			} else {
				return opts, fmt.Errorf("unknown flag: %s", arg)
			}
		}
	}
	for _, t := range opts.targets {
		if !sdkValidTargets[t] {
			return opts, fmt.Errorf("--target: unknown target %q (valid: go, js)", t)
		}
	}
	return opts, nil
}

// applySDKConfigDefaults fills unset options from a discovered codegen
// config's generator entry named "sdk" (flags always win), so projects can
// pin their SDK settings in gofastr.codegen.yml:
//
//	codegen:
//	  generators:
//	    - name: sdk
//	      output: sdk
//	      config: {targets: [go, js], name: myapp, base_url: "https://api.example.com"}
//
// The same entry drives `gofastr generate --config` through the registered
// in-process generator (sdkGenerator), so both invocation paths agree.
func applySDKConfigDefaults(opts *sdkOptions) {
	discovery, err := codegen.DiscoverConfig(".")
	if err != nil || !discovery.Found {
		return
	}
	for _, gen := range discovery.Config.Codegen.Generators {
		if gen.Name != "sdk" || gen.Extension != "" {
			continue
		}
		cfgString := func(key string) string {
			if v, ok := gen.Config[key].(string); ok {
				return strings.TrimSpace(v)
			}
			return ""
		}
		if opts.outDir == "" {
			out := gen.Output
			if out == "" {
				out = "sdk"
			}
			opts.outDir = filepath.ToSlash(filepath.Join(discovery.Config.Codegen.Output, out))
		}
		if opts.name == "" {
			opts.name = cfgString("name")
		}
		if opts.apiPrefix == "" {
			opts.apiPrefix = cfgString("api_prefix")
		}
		if opts.module == "" {
			opts.module = cfgString("module")
		}
		if opts.baseURL == "" {
			opts.baseURL = cfgString("base_url")
		}
		if opts.sdkVersion == "" {
			opts.sdkVersion = cfgString("sdk_version")
		}
		if len(opts.targets) == 0 {
			if raw, ok := gen.Config["targets"].([]any); ok {
				for _, t := range raw {
					if s, ok := t.(string); ok {
						opts.targets = append(opts.targets, strings.TrimSpace(s))
					}
				}
			}
		}
		if len(opts.only) == 0 {
			if v := cfgString("only"); v != "" {
				opts.only = splitCommaList(v)
			}
		}
		if len(opts.exclude) == 0 {
			if v := cfgString("exclude"); v != "" {
				opts.exclude = splitCommaList(v)
			}
		}
		return
	}
}

// buildSDKSpec validates selection and derives the renderer input. It
// mutates opts only to settle defaults (name, out dir, targets).
func buildSDKSpec(decls []framework.EntityDeclaration, opts *sdkOptions) (sdkSpec, error) {
	for _, sel := range [][]string{opts.only, opts.exclude} {
		for _, name := range sel {
			if !cliAnyEntityMatches(decls, name) {
				return sdkSpec{}, fmt.Errorf("--only/--exclude: no entity named %q", name)
			}
		}
	}
	for _, t := range opts.targets {
		if !sdkValidTargets[t] {
			return sdkSpec{}, fmt.Errorf("--target: unknown target %q (valid: go, js)", t)
		}
	}
	if len(opts.targets) == 0 {
		opts.targets = []string{"go", "js"}
	}
	sort.Strings(opts.targets)
	if opts.outDir == "" {
		opts.outDir = "gen/sdk"
	}

	abs, err := filepath.Abs(".")
	if err != nil {
		return sdkSpec{}, err
	}
	modulePath, moduleRoot := findEnclosingGoMod(abs)
	if opts.name == "" {
		// The module path base names the app better than the directory
		// (temp dirs, checkouts named "src", …).
		if modulePath != "" {
			opts.name = strings.ToLower(filepath.Base(filepath.FromSlash(modulePath)))
		} else {
			opts.name = strings.ToLower(filepath.Base(abs))
		}
	}
	if opts.module == "" {
		opts.module = "local/" + strings.ReplaceAll(strings.ToLower(opts.name), "_", "-") + "-sdk"
	}
	if opts.sdkVersion == "" {
		opts.sdkVersion = "0.0.0-dev"
	}

	gofastrVersion := "dev"
	if moduleRoot != "" {
		if v, _, err := goModGofastrVersion(moduleRoot); err == nil && v != "" {
			gofastrVersion = v
		}
	}

	apiPrefix := strings.Trim(strings.TrimSpace(opts.apiPrefix), "/")
	if apiPrefix != "" {
		apiPrefix = "/" + apiPrefix
	}

	spec := sdkSpec{
		App:            opts.name,
		SDKVersion:     opts.sdkVersion,
		GofastrVersion: gofastrVersion,
		Module:         opts.module,
		BaseURL:        opts.baseURL,
		APIPrefix:      apiPrefix,
	}

	matchesAny := func(decl framework.EntityDeclaration, names []string) bool {
		for _, name := range names {
			if cliEntityMatches(decl, name) {
				return true
			}
		}
		return false
	}
	for _, decl := range decls {
		if len(opts.only) > 0 && !matchesAny(decl, opts.only) {
			continue
		}
		if matchesAny(decl, opts.exclude) {
			continue
		}
		spec.Decls = append(spec.Decls, decl)
		spec.Entities = append(spec.Entities, buildEntityModel(decl, cliVerbs))
	}
	if len(spec.Decls) == 0 {
		return sdkSpec{}, fmt.Errorf("selection matches no entities — nothing to generate")
	}
	return spec, nil
}

// renderSDKFiles produces every generated file, out-dir-relative: the go/
// and js/ source trees for the requested targets, plus dist/ (archives +
// manifest) unless archives are disabled.
func renderSDKFiles(spec sdkSpec, targets []string, dist bool) ([]generatedFile, error) {
	want := map[string]bool{}
	for _, t := range targets {
		want[t] = true
	}

	var files []generatedFile
	artifacts := map[string]sdk.Artifact{}
	var distFiles []generatedFile

	record := func(key, file, module string, data []byte) {
		sum := sha256.Sum256(data)
		artifacts[key] = sdk.Artifact{File: file, SHA256: fmt.Sprintf("%x", sum), Bytes: int64(len(data)), Module: module}
		distFiles = append(distFiles, generatedFile{name: "dist/" + file, content: string(data)})
	}

	if want["go"] {
		goFiles, err := renderSDKGoFiles(spec)
		if err != nil {
			return nil, err
		}
		for _, f := range goFiles {
			files = append(files, generatedFile{name: "go/" + f.name, content: f.content})
		}
		if dist {
			var zipFiles []sdk.File
			for _, f := range goFiles {
				zipFiles = append(zipFiles, sdk.File{Path: f.name, Data: []byte(f.content)})
			}
			raw, err := sdk.PackZip(spec.App+"-sdk", zipFiles)
			if err != nil {
				return nil, err
			}
			record("go", sdk.GoArtifact, spec.Module, raw)
		}
	}

	if want["js"] {
		jsFiles := renderSDKJSFiles(spec)
		for _, f := range jsFiles {
			files = append(files, generatedFile{name: "js/" + f.name, content: f.content})
			if !dist {
				continue
			}
			switch f.name {
			case sdk.JSArtifact:
				record("js", sdk.JSArtifact, "", []byte(f.content))
			case sdk.JSTypesArtifact:
				record("js-types", sdk.JSTypesArtifact, "", []byte(f.content))
			}
		}
	}

	if dist {
		var names []string
		for _, decl := range spec.Decls {
			names = append(names, decl.Name)
		}
		sort.Strings(names)
		hash, err := sdkSchemaHash(spec.Decls)
		if err != nil {
			return nil, err
		}
		manifest := sdk.Manifest{
			SchemaVersion:  sdk.SchemaVersion,
			App:            spec.App,
			SDKVersion:     spec.SDKVersion,
			GofastrVersion: spec.GofastrVersion,
			GeneratedAt:    time.Now().UTC(),
			Entities:       names,
			SchemaHash:     hash,
			Artifacts:      artifacts,
		}
		raw, err := json.MarshalIndent(manifest, "", "  ")
		if err != nil {
			return nil, err
		}
		distFiles = append(distFiles, generatedFile{name: "dist/" + sdk.ManifestFile, content: string(raw) + "\n"})
		files = append(files, distFiles...)
	}
	return files, nil
}

// sdkSchemaHash computes the drift-detection hash over the selected
// declarations — the generation-time half of the check sdkdocs.Mount
// recomputes against the live registry.
func sdkSchemaHash(decls []framework.EntityDeclaration) (string, error) {
	var named []sdk.NamedConfig
	for _, decl := range decls {
		cfg, err := decl.Config()
		if err != nil {
			return "", fmt.Errorf("entity %q: %w", decl.Name, err)
		}
		named = append(named, sdk.NamedConfig{Name: decl.Name, Config: cfg})
	}
	return sdk.SchemaHash(named), nil
}

// renderSDKGoFiles emits the standalone Go module: the renderClient output
// (identical to the in-app entities/client package) plus go.mod and README.
// client.go is gofmt'd here so the zipped bytes match the written tree.
func renderSDKGoFiles(spec sdkSpec) ([]generatedFile, error) {
	clientSrc := "// " + spec.Header() + "\n// Regenerate: gofastr generate sdk\n\n" + renderClient(spec.Decls)
	formatted, err := format.Source([]byte(clientSrc))
	if err != nil {
		return nil, fmt.Errorf("generated client.go does not parse: %w", err)
	}
	goMod := fmt.Sprintf("module %s\n\ngo 1.21\n", spec.Module)
	return []generatedFile{
		{name: "client.go", content: string(formatted)},
		{name: "go.mod", content: goMod},
		{name: "README.md", content: renderSDKGoReadme(spec)},
	}, nil
}

func sdkExampleBaseURL(spec sdkSpec) string {
	return sdkExampleHost(spec) + spec.APIPrefix
}

// sdkExampleHost is the app origin WITHOUT the API prefix — docs-site and
// download URLs mount on the host, only API calls live under the prefix.
func sdkExampleHost(spec sdkSpec) string {
	base := spec.BaseURL
	if base == "" {
		base = "https://your-app.example.com"
	}
	return strings.TrimRight(base, "/")
}

func renderSDKGoReadme(spec sdkSpec) string {
	first := spec.Entities[0]
	var sb strings.Builder
	fmt.Fprintf(&sb, "# %s Go SDK\n\n<!-- %s -->\n\n", spec.App, spec.Header())
	fmt.Fprintf(&sb, "A standalone, stdlib-only Go client for the %s HTTP API.\n\n", spec.App)
	sb.WriteString("## Install\n\nUnzip next to your project (or anywhere), then wire the module locally:\n\n")
	fmt.Fprintf(&sb, "```sh\ngo mod edit -replace %s=./%s-sdk\ngo get %s\n```\n\n", spec.Module, spec.App, spec.Module)
	sb.WriteString("## Use\n\n")
	fmt.Fprintf(&sb, "```go\nimport client %q\n\n", spec.Module)
	fmt.Fprintf(&sb, "c := client.NewClient(%q, nil)\nc.Token = os.Getenv(\"API_TOKEN\") // Bearer token minted via POST /auth/tokens\n\n", sdkExampleBaseURL(spec))
	fmt.Fprintf(&sb, "page, err := c.List%s(ctx, nil)\n```\n\n", first.Struct)
	sb.WriteString("The base URL must include the API prefix when the server mounts one (e.g. `/api`).\n\n")
	sb.WriteString("## Filtering, sorting, pagination\n\n")
	sb.WriteString("List methods take `url.Values`. Query parameter names are the **snake_case**\ncolumn names (responses are camelCase — that asymmetry is the server's contract):\n\n")
	filterField := first.Fields[0].Snake
	fmt.Fprintf(&sb, "```go\nparams := url.Values{}\nparams.Set(%q, \"x\")        // equality\nparams.Set(%q, \"x\")   // gt/gte/lt/lte/like/in suffixes\nparams.Set(\"sort\", \"-created_at\")\nparams.Set(\"limit\", \"50\")\nc.List%s(ctx, params)\n```\n\n", filterField, filterField+"_gte", first.Struct)
	sb.WriteString("Validation errors return `*APIError`; its `Body` holds the JSON envelope\n`{\"error\", \"success\", \"code\", \"fields\"}` where `fields` keys are snake_case\ncolumn names.\n\n")
	sb.WriteString("`Do(ctx, method, path, body, out)` is the escape hatch for custom endpoints\nand presence-faithful `map[string]any` bodies. `Watch<Entity>` subscribes to\nthe live SSE feed. `Batch*` methods hit the atomic `_batch` routes.\n")
	return sb.String()
}

// sdkGenerator adapts `generate sdk` to the codegen registry so a generator
// entry named "sdk" in gofastr.codegen.yml also runs under
// `gofastr generate --config` (the runner errors on unregistered names).
type sdkGenerator struct{}

func (sdkGenerator) Name() string { return "sdk" }

func (sdkGenerator) Generate(_ context.Context, genCtx *codegen.Context, cfg codegen.GeneratorConfig) ([]codegen.GeneratedFile, error) {
	dir := genCtx.ProjectDir
	if dir == "" {
		dir = "."
	}
	decls, err := packReadEntities(dir)
	if err != nil {
		return nil, err
	}
	if len(decls) == 0 {
		return nil, fmt.Errorf("no entities package found in %s", dir)
	}
	opts := sdkOptions{}
	cfgString := func(key string) string {
		if v, ok := cfg.Config[key].(string); ok {
			return strings.TrimSpace(v)
		}
		return ""
	}
	opts.name = cfgString("name")
	opts.apiPrefix = cfgString("api_prefix")
	opts.module = cfgString("module")
	opts.baseURL = cfgString("base_url")
	opts.sdkVersion = cfgString("sdk_version")
	opts.only = splitCommaList(cfgString("only"))
	opts.exclude = splitCommaList(cfgString("exclude"))
	if raw, ok := cfg.Config["targets"].([]any); ok {
		for _, t := range raw {
			if s, ok := t.(string); ok {
				opts.targets = append(opts.targets, strings.TrimSpace(s))
			}
		}
	}
	// The runner prefixes output itself (cfg.Output); outDir only matters
	// for the direct command path.
	opts.outDir = "gen/sdk"

	spec, err := buildSDKSpec(decls, &opts)
	if err != nil {
		return nil, err
	}
	files, err := renderSDKFiles(spec, opts.targets, true)
	if err != nil {
		return nil, err
	}
	out := make([]codegen.GeneratedFile, 0, len(files))
	for _, f := range files {
		content := f.content
		if strings.HasSuffix(f.name, ".go") {
			if formatted, err := format.Source([]byte(content)); err == nil {
				content = string(formatted)
			}
		}
		out = append(out, codegen.GeneratedFile{Path: f.name, Content: content})
	}
	return out, nil
}
