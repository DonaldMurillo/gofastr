// seedPosts inserts test data and returns the IDs.
func seedPosts(t *testing.T, db *sql.DB, posts ...map[string]string) []string {
	t.Helper()
	var ids []string
	for i, p := range posts {
		id := p["id"]
		if id == "" {
			id = fmt.Sprintf("post-%d", i+1)
		}
		title := p["title"]
		body := p["body"]
		status := p["status"]
		if status == "" {
			status = "draft"
		}
		authorID := p["authorId"]
		_, err := db.Exec("INSERT INTO posts (id, title, body, status, author_id) VALUES ($1, $2, $3, $4, $5)",
			id, title, body, status, authorID)
		if err != nil {
			t.Fatalf("seed post %d: %v", i, err)
		}
		ids = append(ids, id)
	}
	return ids
}

