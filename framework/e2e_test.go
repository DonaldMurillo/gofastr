// E2E: Soft Delete
// ============================================================================

func TestE2E_SoftDelete(t *testing.T) {
	forEachDialect(t, func(t *testing.T, db *sql.DB, dialect Dialect) {

		// Create table with deleted_at column
		_, err := db.Exec(`CREATE TABLE posts (
			id TEXT PRIMARY KEY,
			title TEXT NOT NULL,
			body TEXT DEFAULT '',
			status TEXT DEFAULT 'draft',
			author_id TEXT DEFAULT '',
			deleted_at TIMESTAMP DEFAULT NULL,
			created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
			updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
		)`)
		if err != nil {
			t.Fatalf("create table: %v", err)
		}

		app := NewApp(WithDB(db))
		ent := entity.Define("posts", entity.EntityConfig{
			Table:      "posts",
			SoftDelete: true,
			Fields: []schema.Field{
				{Name: "title", Type: schema.String, Required: true},
				{Name: "body", Type: schema.Text},
				{Name: "status", Type: schema.String},
				{Name: "author_id", Type: schema.String},
			},
		})
		app.Registry.Register(ent)

		ch := crud.NewCrudHandler(ent, db)
		crud.RegisterCrudRoutes(app.Router(), ch, "/posts")

		ta := TestHarness(t, app)
		defer ta.Close()

		// Create a post (server generates ID)
		resp := ta.Post("/posts", map[string]string{
			"title": "Soft Delete Me",
		})
		resp.AssertStatus(t, http.StatusCreated)

		var created map[string]any
		if err := resp.JSON(&created); err != nil {
			t.Fatalf("decode: %v", err)
		}
		id := created["id"].(string)

		// Soft delete
		resp = ta.Delete("/posts/" + id)
		resp.AssertStatus(t, http.StatusNoContent)

		// Verify deleted_at is set in DB
		var deletedAt sql.NullString
		err = db.QueryRow("SELECT deleted_at FROM posts WHERE id = $1", id).Scan(&deletedAt)
		if err != nil {
			t.Fatalf("query deleted_at: %v", err)
		}
		if !deletedAt.Valid {
			t.Error("expected deleted_at to be set, but it's NULL")
		}

		// Row still exists
		var count int
		db.QueryRow("SELECT COUNT(*) FROM posts WHERE id = $1", id).Scan(&count)
		if count != 1 {
			t.Errorf("expected soft-deleted row to still exist, count=%d", count)
		}
	})
}

