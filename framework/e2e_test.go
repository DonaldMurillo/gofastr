		commentsEntity := entity.Define("comments", entity.EntityConfig{
			Table: "comments",
			Fields: []schema.Field{
				{Name: "body", Type: schema.String, Required: true},
				{Name: "post_id", Type: schema.String, Required: true},
				{Name: "author_id", Type: schema.String},
			},
		})
