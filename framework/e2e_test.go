// setupTestDB returns a database for the given dialect. Uses the dual-dialect
// harness so each test fans out to both engines.
func setupTestDB(t *testing.T, dialect Dialect) *sql.DB {
	t.Helper()
	return openTestDB(t, dialect)
}

// createPostsTable creates the posts table on the given DB. DDL is portable:
// TEXT, TIMESTAMP, and CURRENT_TIMESTAMP are accepted by both engines (PG
// rejects the older DATETIME alias which was used before the dual-dialect
// migration).
func createPostsTable(t *testing.T, db *sql.DB) {
	t.Helper()
	_, err := db.Exec(`CREATE TABLE IF NOT EXISTS posts (
		id TEXT PRIMARY KEY,
		title TEXT NOT NULL,
		body TEXT DEFAULT '',
		status TEXT DEFAULT 'draft',
		author_id TEXT DEFAULT '',
		created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
		updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
	)`)
	if err != nil {
		t.Fatalf("create posts table: %v", err)
	}
}

